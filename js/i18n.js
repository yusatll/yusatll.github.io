// ── i18n Engine ──
const i18n = {
    currentLang: localStorage.getItem('vayt-lang') || 'tr',

    init() {
        this.applyLanguage(this.currentLang);
        this.updateToggleUI();
    },

    toggle() {
        this.currentLang = this.currentLang === 'tr' ? 'en' : 'tr';
        localStorage.setItem('vayt-lang', this.currentLang);
        this.applyLanguage(this.currentLang);
        this.updateToggleUI();
    },

    applyLanguage(lang) {
        const t = translations[lang];
        if (!t) return;

        // Update html lang attribute
        document.documentElement.lang = lang;

        // Translate all elements with data-i18n
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (t[key]) {
                el.textContent = t[key];
            }
        });

        // Translate placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            if (t[key]) {
                el.placeholder = t[key];
            }
        });

        // Update contact form sent message reference
        window._i18nSentText = t['contact.sent'];
    },

    updateToggleUI() {
        const btn = document.getElementById('langToggle');
        if (btn) {
            const inactive = this.currentLang === 'tr' ? 'EN' : 'TR';
            btn.querySelector('.lang-active').textContent = this.currentLang.toUpperCase();
            btn.querySelector('.lang-inactive').textContent = inactive;
        }
    }
};
